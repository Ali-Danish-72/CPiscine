#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	str_swap(char **s1, char **s2)
{
	char	*str;

	str = *s1;
	*s1 = *s2;
	*s2 = str;
}

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] == s2[i])
			i++;
		else
			return (s1[i] - s2[i]);
	}
	return (0);
}

void	print_result(int argc, char **argv)
{
	int	i;

	i = 1;
	while (i < argc)
	{
		ft_putstr(argv[i]);
		write(1, "\n", 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 1;
	while (i < argc)
	{
		j = i;
		while (j > 1 && ft_strcmp(argv[j], argv[j - 1]) < 0)
		{
			str_swap(&argv[j], &argv[j - 1]);
			j--;
		}
		i++;
	}
	print_result(argc, argv);
}
